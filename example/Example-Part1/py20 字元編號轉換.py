# 字元編號轉換
x1 = 113
x2 = chr(x1)      
print(x2)             # 輸出 ascii 編號113的字元'q'

x3 = ord(x2)
print(x3)             # 輸出字元x3的 ascii 碼值

x4 = '文'
print(ord(x4))        # 輸出字元'文'的 Unicode 碼值

