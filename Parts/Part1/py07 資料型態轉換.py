'''
py03: 資料型態轉換
'''
a = 5
print(a), print(type(a))   # 列出資料型態
b = 5.3
print(b), print(type(b))   # 列出資料型態
c = int(b)
print(c), print(type(c))    
d = float(a)
print(d), print(type(d))  # 列出資料型態
e = 'x'
print(e), print(type(e))  # 列出資料型態
f = 'xyz'
print(f), print(type(f))  # 列出資料型態
g = [1,2,3]
print(g), print(type(g))  # 列出資料型態

x = True
print(x), print(type(x))      # 列出x資料型態
y = False
print(y), print(type(y))      # 列出y資料型態



